package trytest.thymeleaf.bean;

public class Car {
	int id;
	String name;
	int hp ;
	
	public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
	public int getHp() {
        return hp;
    }
    public void setHp(int hp) {
        this.hp = hp;
    }
	
}
